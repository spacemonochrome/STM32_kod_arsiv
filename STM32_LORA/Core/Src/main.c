void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	HAL_UART_Receive_IT(&huart3, (uint8_t*)Rx_Byte, 1);

	if(Rx_Cnt==0) memset(Uart_Data,0x00,13);

	Uart_Data[Rx_Cnt++] = Rx_Byte[0];

	if(Rx_Cnt>=13)
	{
		if( (Uart_Data[11]=='\r') &&  (Uart_Data[12]=='\n') )
		{
			Rx_Cnt = 0;
			Rx_Flag = 1;
			//HAL_UART_Transmit_IT(&huart3,Uart_Data,13);
			HAL_UART_Transmit_IT(&huart3,"Message Received...",19);
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
		}
		else
		{
			Rx_Cnt = 0;
			memset(Uart_Data,0x00,13);
		}

	}

}
